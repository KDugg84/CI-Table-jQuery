$(document).ready(function() {
    $("h2").addClass("underline"); //Underlines all <h2> elements
    $("ul").addClass("border"); //Adds a border to all lists
});

// Will remove that class from the elements 
$(document).ready(function() {
    $("h2").removeClass("underline"); //Underlines all <h2> elements
    $("ul").removeClassClass("border"); //Adds a border to all lists
});